#!/bin/bash

# rd_nixnan_experiment.sh
# Experimental workflow for precision-edge CUDA demo with nixnan monitoring
# Tests floating-point precision limits across FP16, BF16, and FP32 formats
# using a reaction-diffusion equation (FTCS discretization)
#
# Demonstrates:
#  - Per-format exception detection (overflow, underflow, NaN propagation)
#  - Exponent histogram bucketing by format
#  - Early-warning tripwire detection before overflow
#  - Source-line attribution of inf/NaN events
#
# Traces are captured to traces/Stage{2,3,4,5}/ for analysis and archival.

set -e

# Configuration
NIXNAN_SO="${NIXNAN_PATH:-./nixnan.so}"
CUDA_ARCH="sm_80"  # A100 GPU; adjust to sm_86 if targeting Ampere H100
EXECUTABLE="./rd_nixnan"
SPEC_FILE="./spec.json"
TRACES_DIR="./traces"

echo "========================================"
echo "rd_nixnan Precision-Edge Demo Workflow"
echo "========================================"

# ============================================================================
# STAGE 1: Build the CUDA kernels (three formats: f16, bf16, f32)
# ============================================================================
echo ""
echo "[1/5] Building CUDA executable..."
echo ""
echo "BUILD CONFIGURATION:"
echo "  - Architecture: $CUDA_ARCH (A100)"
echo "  - NO lineinfo flag (unreliable; SASS instructions checked separately via cuobjdump)"
echo "  - Three kernels: FP16, BF16, FP32 (separate to avoid format interleaving)"
echo ""
echo "WHY:"
echo "  Each format overflows at different step counts (FP16 ~227, BF16/FP32 ~1818)."
echo "  Separate kernels ensure clean per-format exponent attribution in nixnan."
echo "  Lineinfo is disabled; we verify SASS separately if needed."
echo ""
echo "Command: nvcc -arch=$CUDA_ARCH rd_nixnan.cu -o $EXECUTABLE"
echo ""
nvcc -arch=$CUDA_ARCH rd_nixnan.cu -o $EXECUTABLE
if [ ! -f "$EXECUTABLE" ]; then
    echo "ERROR: Build failed; $EXECUTABLE not found"
    exit 1
fi
echo "✓ Build successful"

# ============================================================================
# STAGE 2: Baseline run — no instrumentation (observe native behavior)
# ============================================================================
echo ""
echo "=========================================="
echo "[2/5] STAGE 2: Baseline run (no instrumentation)"
echo "=========================================="
echo ""
echo "STAGE PURPOSE:"
echo "  Observe native overflow behavior WITHOUT any nixnan instrumentation."
echo "  Each format (FP16, BF16, FP32) runs the same reaction-diffusion PDE but"
echo "  reaches machine epsilon at different wallclock step counts."
echo ""
echo "EXPECTED BEHAVIOR:"
echo "  • FP16 (5-bit exponent, max ~65K):"
echo "    - Overflows to +inf at ~step 227 (t ≈ 0.23 seconds)"
echo "    - Stencil u[i+1] - 2*u[i] + u[i-1] → inf - 2*inf + inf → NaN"
echo "    - max_element() silent failure: all NaN comparisons fail, returns begin()"
echo "    - Reported max: 0.0 (boundary pinned to 0, not inf or NaN!)"
echo ""
echo "  • BF16 (8-bit exponent, matches FP32 range):"
echo "    - Overflows to +inf at ~step 1818 (t ≈ 1.82 seconds)"
echo "    - Diffusion term ~2^-8 · u (8-bit mantissa noise)"
echo "    - Reaction dominates, diffusion is ephemeral"
echo ""
echo "  • FP32 (8-bit exponent, 24-bit mantissa):"
echo "    - Overflows to +inf at ~step 1818 (same as BF16)"
echo "    - Diffusion signal is clean, strong contrast with BF16"
echo "    - This contrast is the teaching moment for FP32 vs BF16"
echo ""
echo "TRACING:"
echo "  Outputs (stdout) saved to: $TRACES_DIR/Stage2/baseline_native.txt"
echo "  No nixnan data (uninstrumented run)."
echo ""
mkdir -p "$TRACES_DIR/Stage2"
$EXECUTABLE 2>&1 | tee "$TRACES_DIR/Stage2/baseline_native.txt"
echo ""
echo "✓ Stage 2 complete. Baseline captured."

# ============================================================================
# STAGE 3: Exception summary per format (LD_PRELOAD nixnan.so)
# ============================================================================
echo ""
echo "=========================================="
echo "[3/5] STAGE 3: Exception summary per format (LD_PRELOAD)"
echo "=========================================="
echo ""
echo "STAGE PURPOSE:"
echo "  Instrument with nixnan.so to capture per-format exception counters."
echo "  nixnan buckets exceptions by SASS instruction format (f16/bf16/f32/f64)."
echo ""
echo "CONFIGURATION:"
echo "  - LD_PRELOAD=$NIXNAN_SO"
echo "  - NO environment flags (baseline instrumentation)"
echo "  - Counters: Overflow (Inf), Underflow (Subnormal), NaN propagation"
echo ""
echo "WHY THIS APPROACH:"
echo "  Exception counters are coarse (per-format, not per-kernel), but fast."
echo "  FP16 will show SUBNORMAL counts (unique to low precision):"
echo "    - Diffusion increment r * (u[i+1] - 2*u[i] + u[i-1]) ≈ 2^-13 * u"
echo "    - Near-boundary nodes: 2^-13 * u drops below 2^-14 (FP16 subnorm floor)"
echo "    - Diffusion is silently flushed; reaction unchecked."
echo ""
echo "  BF16/FP32 will show no subnormals (range matches FP32)."
echo ""
echo "EXPECTED COUNTERS:"
echo "  f16:  {Inf: ~1, Subnormal: ~many, NaN: ~lots}"
echo "  bf16: {Inf: ~1, Subnormal: 0, NaN: ~lots}"
echo "  f32:  {Inf: ~1, Subnormal: 0, NaN: ~lots}"
echo ""
echo "TRACING:"
echo "  nixnan exception output saved to: $TRACES_DIR/Stage3/exceptions_summary.txt"
echo ""
mkdir -p "$TRACES_DIR/Stage3"
LD_PRELOAD=$NIXNAN_SO $EXECUTABLE 2>&1 | tee "$TRACES_DIR/Stage3/exceptions_summary.txt"
echo ""
echo "✓ Stage 3 complete. Exception summary captured."

# ============================================================================
# STAGE 4: Exponent histogram per format (HISTOGRAM=1)
# ============================================================================
echo ""
echo "=========================================="
echo "[4/5] STAGE 4: Exponent histogram bucketing"
echo "=========================================="
echo ""
echo "STAGE PURPOSE:"
echo "  Enable HISTOGRAM=1 to see the full exponent distribution for each format."
echo "  This reveals which exponent binades are populated over the simulation."
echo ""
echo "CONFIGURATION:"
echo "  - HISTOGRAM=1          (enable exponent bucketing)"
echo "  - LD_PRELOAD=$NIXNAN_SO"
echo "  - NO spec.json (all bins reported)"
echo ""
echo "WHY THIS APPROACH:"
echo "  Exponent histograms show precision _range_, not just overflow."
echo "  Contrast the three formats reveals the teaching moments:"
echo ""
echo "  FP16 (5-bit exp, max=15):"
echo "    - Exponent range: [-14, 15] (saturates at max, then inf)"
echo "    - Subnormals occupy [-14, -15, ...] as amplitude shrinks"
echo "    - Stops early; limited dynamic range forces saturation"
echo ""
echo "  BF16 (8-bit exp, max=127):"
echo "    - Exponent range: [-5, 127] (full range, rides high exponents)"
echo "    - BUT: 8-bit mantissa means diffusion term is noise (2^-8 precision)"
echo "    - Histogram width (range) looks healthy; precision failure hidden"
echo ""
echo "  FP32 (8-bit exp, max=127):"
echo "    - Exponent range: [-5, 127] (identical to BF16)"
echo "    - 24-bit mantissa: diffusion is signal, not noise"
echo "    - BF16 vs FP32 at same exponent range = hidden precision failure in BF16"
echo ""
echo "EXPECTED HISTOGRAMS:"
echo "  Exponent range for f16:  [-14, 15]"
echo "  Exponent range for bf16: [-5, 127]"
echo "  Exponent range for f32:  [-5, 127]"
echo ""
echo "TRACING:"
echo "  Histogram data + exception counts saved to: $TRACES_DIR/Stage4/histogram_full.txt"
echo ""
mkdir -p "$TRACES_DIR/Stage4"
HISTOGRAM=1 LD_PRELOAD=$NIXNAN_SO $EXECUTABLE 2>&1 | tee "$TRACES_DIR/Stage4/histogram_full.txt"
echo ""
echo "✓ Stage 4 complete. Histogram data captured."

# ============================================================================
# STAGE 5: Pre-overflow tripwire with spec.json
# ============================================================================
echo ""
echo "=========================================="
echo "[5/5] STAGE 5: Pre-overflow tripwire detection"
echo "=========================================="
echo ""
echo "STAGE PURPOSE:"
echo "  Use spec.json to define bin thresholds (tripwires)."
echo "  When values enter high binades, fire warnings _before_ overflow."
echo "  This gives early warning and a cleaner tutorial arc than corpse analysis."
echo ""
echo "CONFIGURATION:"
echo "  - BIN_SPEC_FILE=$SPEC_FILE  (thresholds defined in spec.json)"
echo "  - HISTOGRAM=1               (full histogram + tripwire warnings)"
echo "  - LD_PRELOAD=$NIXNAN_SO"
echo ""
echo "SPEC.JSON THRESHOLDS:"
echo "  f16:  bins [13, 15] report_frequency=5  (fire warnings in top 3 binades)"
echo "  bf16: bins [120, 127] report_frequency=5 (top 8 binades)"
echo "  f32:  bins [120, 127] report_frequency=5 (top 8 binades)"
echo ""
echo "WHY THIS APPROACH:"
echo "  Staged warnings give time to analyze behavior BEFORE saturation."
echo "  A few steps before inf, we flag values entering the danger zone."
echo "  Nicer narrative: detect anomaly, then watch it blow up (vs. post-mortem analysis)."
echo ""
echo "EXPECTED WARNINGS:"
echo "  f16:  'Bin [13,15] tripwire fired' at step ~220 (7 steps before overflow)"
echo "  bf16: 'Bin [120,127] tripwire fired' at step ~1810 (8 steps before overflow)"
echo "  f32:  'Bin [120,127] tripwire fired' at step ~1810"
echo ""
echo "TRACING:"
echo "  Tripwire alerts + histogram saved to: $TRACES_DIR/Stage5/histogram_tripwire.txt"
echo ""
mkdir -p "$TRACES_DIR/Stage5"
BIN_SPEC_FILE=$SPEC_FILE HISTOGRAM=1 LD_PRELOAD=$NIXNAN_SO $EXECUTABLE 2>&1 | tee "$TRACES_DIR/Stage5/histogram_tripwire.txt"
echo ""
echo "✓ Stage 5 complete. Tripwire data captured."

# ============================================================================
# OPTIONAL: Full instrumentation with INSTR_MEM + LINE_INFO
# ============================================================================
# Uncomment if you want full SASS instruction memory + source-line tracking.
# This tracks every FLOPs and catches the exact stencil (inf - 2·inf + inf → NaN).
# Overhead: 50× NVBit multiplier, but still sub-second for ~7.4e5 step-ops.
#
# echo ""
# echo "=========================================="
# echo "[OPTIONAL] Full instrumentation (INSTR_MEM + LINE_INFO)"
# echo "=========================================="
# echo ""
# echo "CONFIGURATION:"
# echo "  - INSTR_MEM=1  (track all SASS instructions in memory)"
# echo "  - LINE_INFO=1  (source-line attribution; requires nvcc -lineinfo)"
# echo "  - HISTOGRAM=1  (per-format histogram)"
# echo "  - LD_PRELOAD=$NIXNAN_SO"
# echo ""
# echo "WHY:"
# echo "  Catches the exact stencil operation that produces NaN."
# echo "  Source-line attribution for slides/visualization."
# echo "  Heavy overhead (50×), but justified for detailed post-mortem."
# echo ""
# echo "TRACING:"
# echo "  Full SASS dump + exceptions saved to: $TRACES_DIR/StageOptional/instr_full.txt"
# echo ""
# mkdir -p "$TRACES_DIR/StageOptional"
# INSTR_MEM=1 LINE_INFO=1 HISTOGRAM=1 LD_PRELOAD=$NIXNAN_SO $EXECUTABLE 2>&1 | tee "$TRACES_DIR/StageOptional/instr_full.txt"
# echo ""
# echo "✓ Optional instrumentation complete."

echo ""
echo "========================================"
echo "WORKFLOW COMPLETE"
echo "========================================"
echo ""
echo "TRACE LOCATION:"
echo "  All outputs archived in: $TRACES_DIR/"
echo ""
echo "  $TRACES_DIR/Stage2/  - Baseline (uninstrumented) native behavior"
echo "  $TRACES_DIR/Stage3/  - Exception counters per format"
echo "  $TRACES_DIR/Stage4/  - Full exponent histograms"
echo "  $TRACES_DIR/Stage5/  - Tripwire warnings + histograms"
echo ""
echo "INTERPRETATION GUIDE:"
echo "============================================"
echo "  FP16:"
echo "    ✓ Overflow to +inf (~step 227)"
echo "    ✓ Subnormal count (low-precis diffusion flushed)"
echo "    ✓ NaN propagation (stencil 'inf - 2*inf + inf')"
echo "    ✓ max_element() silent failure → reports 0.0 (not inf, not NaN!)"
echo ""
echo "  BF16:"
echo "    ✓ Overflow to +inf (~step 1818)"
echo "    ✓ NO subnormals (range matches FP32)"
echo "    ✓ NaN propagation"
echo "    ✓ Diffusion term: ~2^-8 (8-bit mantissa = noise, not signal)"
echo ""
echo "  FP32:"
echo "    ✓ Overflow to +inf (~step 1818, same as BF16)"
echo "    ✓ NO subnormals"
echo "    ✓ NaN propagation"
echo "    ✓ Diffusion term: clean signal (24-bit mantissa)"
echo ""
echo "TEACHING MOMENTS:"
echo "============================================"
echo "  1. FP16 subnormals: low precision silently deletes diffusion physics."
echo "  2. BF16 vs FP32 at same exponent range: mantissa is hidden in histograms."
echo "  3. max_element() with NaN: silent failure (all comparisons false)."
echo "  4. Tripwire approach: early warning > post-mortem diagnosis."
echo ""
echo "PARAMETER ADJUSTMENT:"
echo "============================================"
echo "  If FP16 subnormal count is zero, adjust rd_nixnan.cu parameters:"
echo "  - Lower lambda (reaction rate) or increase dt (time step)"
echo "  - Goal: boundary-node diffusion increment drops below 2^-14"
echo ""
